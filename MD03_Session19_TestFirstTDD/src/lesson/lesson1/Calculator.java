package lesson.lesson1;

public class Calculator {
    //Phương thức int add(int a, int b) thực hiện phép cộng hai số nguyên a và b và trả về kết quả
    //
    //Phương thức int subtract(int a, int b) thực hiện phép trừ hai số nguyên a và b và trả về kết quả.
    public int add(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
}
